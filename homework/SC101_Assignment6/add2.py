import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    n_l1 = listnode_to_int(l1)
    n_l2 = listnode_to_int(l2)
    n_l3 = n_l1 + n_l2
    l3 = int_to_listnode(n_l3)
    return l3


def listnode_to_int(l_l: ListNode) -> int:
    cur = l_l
    count = 0  # To count the number of digits
    while cur is not None:
        count += 1
        cur = cur.next
    cur = l_l
    n = 0
    for i in range(count):
        n += cur.val * (10 ** i)
        cur = cur.next
    return n


def int_to_listnode(n: int) -> ListNode:
    l_l = ListNode(n % 10, None)
    n //= 10
    while n != 0:
        val = n % 10
        new_node = ListNode(val, None)
        cur = l_l
        while cur.next is not None:
            cur = cur.next
        cur.next = new_node
        n //= 10
    return l_l


